"""
Standalone Ragas evaluation script.
Replicates the evaluate_faithfulness logic from analytics.py without Streamlit.
Run with: python run_ragas_eval.py

Sync notes (vs analytics.py):
  - clean_answer_for_eval() and format_brief_to_text() are imported from analytics.py
    to avoid duplication — analytics.py is the single source of truth for these helpers.
  - _extract_answer() mirrors the helper defined inside analytics.evaluate_faithfulness().
  - Each test case gets a fresh create_agent() instance + unique thread_id suffix to
    prevent LangGraph MemorySaver checkpoint collisions across sequential runs.
  - has_client_ref uses any() to match the analytics.py form.
  - RunConfig keeps standalone-friendly values (max_workers=2, max_retries=15, timeout=240).
"""
import os
import json
import sys
import types
import math

# Stub out vertexai module to prevent import errors in ragas
if "langchain_community.chat_models.vertexai" not in sys.modules:
    m = types.ModuleType("vertexai")
    m.ChatVertexAI = None
    sys.modules["langchain_community.chat_models.vertexai"] = m

from dotenv import load_dotenv
load_dotenv()

from datasets import Dataset
from ragas import evaluate
from ragas.metrics._faithfulness import Faithfulness
from ragas.metrics._answer_relevance import ResponseRelevancy
from ragas.llms import llm_factory
from ragas.embeddings import LangchainEmbeddingsWrapper
from ragas.run_config import RunConfig
from langchain_openai import OpenAIEmbeddings
from openai import OpenAI as OpenAIClient

from client_db import get_client_profile
from agent import create_agent

# ── Import shared helpers from analytics.py (single source of truth) ──────────
# Avoids duplicating clean_answer_for_eval() and format_brief_to_text() here.
from analytics import clean_answer_for_eval, format_brief_to_text


# ── Answer extraction helper — mirrors analytics.py _extract_answer() ─────────
def _extract_answer(final_state, query):
    """
    Convert a final agent state into a single answer string suitable for Ragas.
    Matches the _extract_answer() closure inside analytics.evaluate_faithfulness().
    """
    if final_state.get("free_form_response"):
        raw_answer = final_state["free_form_response"]
        c_id = final_state.get("client_id", "client")
        query_lower = query.lower()

        client_name = "client"
        if c_id:
            c_id_up = c_id.upper()
            if "C-204" in c_id_up:
                client_name = "Eleanor Vance"
            elif "C-101" in c_id_up:
                client_name = "Arthur Pendleton"
            elif "C-302" in c_id_up:
                client_name = "Marcus Vance"

        # Use any() — aligned to analytics.py form
        has_client_ref = any(
            kw in query_lower for kw in ("client", "vance", "pendleton", "c-")
        )

        if has_client_ref:
            if "talking point" in query_lower:
                prefix = f"Talking points prepared for client {client_name} ({c_id}):\n"
            elif any(kw in query_lower for kw in ("suitab", "recommend", "adjust")):
                prefix = f"Suitability and investment recommendation assessment for client {client_name} ({c_id}):\n"
            elif any(kw in query_lower for kw in ("risk", "portfolio", "holding")):
                prefix = f"Portfolio risk and holdings summary review for client {client_name} ({c_id}):\n"
            else:
                prefix = f"General response report for client {client_name} ({c_id}):\n"
        else:
            if "talking point" in query_lower:
                prefix = "General talking points research summary:\n"
            elif any(kw in query_lower for kw in ("suitab", "recommend", "adjust")):
                prefix = "General suitability and investment recommendation guidelines:\n"
            elif any(kw in query_lower for kw in ("risk", "portfolio", "holding")):
                prefix = "General fund risk and term parameters comparison:\n"
            else:
                prefix = "General research information note:\n"

        return prefix + clean_answer_for_eval(raw_answer)

    elif final_state.get("final_brief"):
        return clean_answer_for_eval(
            format_brief_to_text(final_state["final_brief"], query)
        )

    return "No response generated by agent."


def main():
    dataset_path = "eval_dataset.json"
    with open(dataset_path, "r", encoding="utf-8") as f:
        eval_data = json.load(f)

    test_cases = eval_data.get("faithfulness", {}).get("test_cases", [])
    print(f"\nFound {len(test_cases)} test cases in eval_dataset.json\n")

    questions = []
    contexts_list = []
    answers = []
    tool_accuracy_list = []

    for idx, tc in enumerate(test_cases):
        query = tc["query"]
        client_id = tc["client_id"]
        expected_docs = tc.get("expected_source_doc_ids", [])

        print(f"[{idx+1}/{len(test_cases)}] Running: {query[:80]}...")

        profile = get_client_profile(client_id)

        initial_state = {
            "query": query,
            "client_id": client_id,
            "product_code": tc.get("product_code"),
            "allocation_amount": 0.0,
            "force_structured": False,
            "response_mode": "freeform",   # default; classify_intent_node will override
            "client_profile": profile,
            "retrieved_evidence": [],
            "compliance_status": "Cleared",
            "escalated": False,
            "review_notes": None,
            "final_brief": None,
            "free_form_response": None,
            "is_out_of_context": None,
            "plan": None,
            "suitability_report": None,
            "draft_brief": None,
        }

        # Fresh agent instance per test case — mirrors analytics.py parallel worker
        # pattern where each thread gets its own agent to avoid shared-state issues.
        # Unique thread_id suffix prevents MemorySaver checkpoint collisions.
        thread_agent = create_agent(interrupt_before_nodes=[])
        config = {"configurable": {"thread_id": f"eval_faith_{client_id}_{idx}_standalone"}}
        final_state = thread_agent.invoke(initial_state, config)

        retrieved_evidence = final_state.get("retrieved_evidence", []) or []
        contexts = [d["content"] for d in retrieved_evidence]
        retrieved_ids = list(
            set([d["doc_id"] for d in retrieved_evidence if d.get("doc_id")])
        )

        if expected_docs:
            hits = [exp for exp in expected_docs if exp in retrieved_ids]
            tool_acc = len(hits) / len(expected_docs)
        else:
            # No expected docs → 100% accuracy only if nothing was returned
            tool_acc = 1.0 if not retrieved_ids else 0.0

        tool_accuracy_list.append(tool_acc)

        # Use the shared _extract_answer helper (matches analytics.py)
        answer = _extract_answer(final_state, query)

        questions.append(query)
        contexts_list.append(contexts)
        answers.append(answer)

        print(f"   Tool Accuracy: {tool_acc*100:.0f}% | Answer length: {len(answer)} chars")

    print("\nRunning Ragas metrics (Faithfulness & Answer Relevancy)...\n")

    openai_api_key = os.environ.get("OPENAI_API_KEY")
    openai_client = OpenAIClient(api_key=openai_api_key)
    ragas_llm = llm_factory("gpt-4o-mini", client=openai_client)

    # Use LangchainEmbeddingsWrapper which inherits from BaseRagasEmbeddings
    lc_emb = OpenAIEmbeddings(model="text-embedding-3-small", api_key=openai_api_key)
    ragas_emb = LangchainEmbeddingsWrapper(lc_emb)

    # Instantiate as dataclass (no constructor args), then assign llm/embeddings as fields
    faith_metric = Faithfulness()
    faith_metric.llm = ragas_llm

    relevancy_metric = ResponseRelevancy()
    relevancy_metric.llm = ragas_llm
    relevancy_metric.embeddings = ragas_emb

    data = {
        "question": questions,
        "contexts": contexts_list,
        "answer": answers,
    }
    dataset = Dataset.from_dict(data)

    # RunConfig: standalone-friendly values kept from original run_ragas_eval.py.
    # analytics.py uses per-sample RunConfig(max_workers=1, max_retries=10, timeout=180)
    # inside threads; here we score the full dataset in one batch call so higher
    # max_workers and retries are appropriate.
    run_config = RunConfig(max_workers=2, max_retries=15, timeout=240)
    result = evaluate(
        dataset=dataset,
        metrics=[faith_metric, relevancy_metric],
        run_config=run_config,
    )

    faith_scores = list(result["faithfulness"])
    relevancy_scores = list(result["answer_relevancy"])

    valid_faith = [s for s in faith_scores if not (isinstance(s, float) and math.isnan(s))]
    valid_rel   = [s for s in relevancy_scores if not (isinstance(s, float) and math.isnan(s))]

    avg_faith     = sum(valid_faith) / len(valid_faith) if valid_faith else 0.0
    avg_relevancy = sum(valid_rel) / len(valid_rel) if valid_rel else 0.0
    avg_tool_acc  = sum(tool_accuracy_list) / len(tool_accuracy_list) if tool_accuracy_list else 0.0

    print("=" * 60)
    print(f"  Ragas Faithfulness:       {avg_faith * 100:.1f}%  ({len(valid_faith)}/{len(faith_scores)} valid)")
    print(f"  Ragas Answer Relevancy:   {avg_relevancy * 100:.1f}%  ({len(valid_rel)}/{len(relevancy_scores)} valid)")
    print(f"  Tool Call Accuracy:       {avg_tool_acc * 100:.1f}%")
    print("=" * 60)

    print("\nPer-Test Breakdown:")
    for i, tc in enumerate(test_cases):
        print(
            f"  [{i+1}] F={faith_scores[i]:.2f} | AR={relevancy_scores[i]:.2f} "
            f"| Tool={tool_accuracy_list[i]*100:.0f}% | Q: {tc['query'][:70]}..."
        )


if __name__ == "__main__":
    main()
